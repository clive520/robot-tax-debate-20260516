drop policy if exists "Users can read own subscription" on public.site_subscriptions;
create policy "Users can read own subscription"
  on public.site_subscriptions
  for select
  to authenticated
  using (auth.uid() = user_id or public.is_admin());

drop policy if exists "Users can subscribe" on public.site_subscriptions;
create policy "Users can subscribe"
  on public.site_subscriptions
  for insert
  to authenticated
  with check (auth.uid() = user_id);

drop policy if exists "Users can update own subscription" on public.site_subscriptions;
create policy "Users can update own subscription"
  on public.site_subscriptions
  for update
  to authenticated
  using (auth.uid() = user_id or public.is_admin())
  with check (auth.uid() = user_id or public.is_admin());

